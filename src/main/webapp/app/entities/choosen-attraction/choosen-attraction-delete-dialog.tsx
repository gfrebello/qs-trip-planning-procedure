import React from 'react';
import { connect } from 'react-redux';
import { RouteComponentProps } from 'react-router-dom';
import { Modal, ModalHeader, ModalBody, ModalFooter, Button } from 'reactstrap';
import { Translate, ICrudGetAction, ICrudDeleteAction } from 'react-jhipster';
import { FontAwesomeIcon } from '@fortawesome/react-fontawesome';

import { IChoosenAttraction } from 'app/shared/model/choosen-attraction.model';
import { IRootState } from 'app/shared/reducers';
import { getEntity, deleteEntity } from './choosen-attraction.reducer';

export interface IChoosenAttractionDeleteDialogProps extends StateProps, DispatchProps, RouteComponentProps<{ id: number }> {}

export class ChoosenAttractionDeleteDialog extends React.Component<IChoosenAttractionDeleteDialogProps> {
  componentDidMount() {
    this.props.getEntity(this.props.match.params.id);
  }

  confirmDelete = event => {
    this.props.deleteEntity(this.props.choosenAttractionEntity.id);
    this.handleClose(event);
  };

  handleClose = event => {
    event.stopPropagation();
    this.props.history.goBack();
  };

  render() {
    const { choosenAttractionEntity } = this.props;
    return (
      <Modal isOpen toggle={this.handleClose}>
        <ModalHeader toggle={this.handleClose}>
          <Translate contentKey="entity.delete.title">Confirm delete operation</Translate>
        </ModalHeader>
        <ModalBody id="tripPlanningApp.choosenAttraction.delete.question">
          <Translate contentKey="tripPlanningApp.choosenAttraction.delete.question" interpolate={{ id: choosenAttractionEntity.id }}>
            Are you sure you want to delete this ChoosenAttraction?
          </Translate>
        </ModalBody>
        <ModalFooter>
          <Button color="secondary" onClick={this.handleClose}>
            <FontAwesomeIcon icon="ban" />
            &nbsp;
            <Translate contentKey="entity.action.cancel">Cancel</Translate>
          </Button>
          <Button id="jhi-confirm-delete-choosenAttraction" color="danger" onClick={this.confirmDelete}>
            <FontAwesomeIcon icon="trash" />
            &nbsp;
            <Translate contentKey="entity.action.delete">Delete</Translate>
          </Button>
        </ModalFooter>
      </Modal>
    );
  }
}

const mapStateToProps = ({ choosenAttraction }: IRootState) => ({
  choosenAttractionEntity: choosenAttraction.entity
});

const mapDispatchToProps = { getEntity, deleteEntity };

type StateProps = ReturnType<typeof mapStateToProps>;
type DispatchProps = typeof mapDispatchToProps;

export default connect(
  mapStateToProps,
  mapDispatchToProps
)(ChoosenAttractionDeleteDialog);
